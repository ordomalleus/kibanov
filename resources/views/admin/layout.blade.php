<script>
    $(document).ready(function () {
        $(".submenu > a").click(function (e) {
            e.preventDefault();
            var $li = $(this).parent("li");
            var $ul = $(this).next("ul");
            if ($li.hasClass("open")) {
                $ul.slideUp(350);
                $li.removeClass("open");
            } else {
                $(".nav > li > ul").slideUp(350);
                $(".nav > li").removeClass("open");
                $ul.slideDown(350);
                $li.addClass("open");
            }
        });

        // Удаляем картинку в товаре
        $(".product-images__delete").click(function (e) {
            e.preventDefault();
            var token = $('meta[name="csrf-token"]').attr('content');
            var id = $(this).attr('data-images-id');

            $.ajax({
                url: 'delete-image/' + id,
                method: 'DELETE',
                headers: {
                    'X-CSRF-TOKEN': token
                }
            }).then((data) => {
                $(this).parent().remove();
            });
        })
    });
</script>
